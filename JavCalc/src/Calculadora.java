public class Calculadora {

    // Atributos da classe
    float resultado;

    void Soma(int n1, int n2){
        resultado = n1+n2;
    }

    void Subtracao(int n1, int n2){
        resultado = n1-n2;
    }

    void Multiplicacao(int n1, int n2){
        resultado = n1*n2;
    }

    void divisao(int n1, int n2){
        resultado = n1/n2;
        
    }

    
}
